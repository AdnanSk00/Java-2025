package tka_april09_Constructor;

public class C1_DeccanBranch {
    public static void main(String[] args) {
        int n = 10;
        for(int i = 1; i <= n; i++){
            System.out.print(i + " ");
        }
        System.out.println();
        int even = 2;
        while(even <= 20){
            System.out.print(even + " ");
            even = even + 2;
        }
        System.out.println();
        int odd = 1;
        while(odd < 20){
            System.out.print(odd + " ");
            odd = odd + 2;
        }

    }
}
