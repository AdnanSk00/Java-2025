public class EvenOrOdd {
	public static void main(String[] args){
	     
        // verify if a number is even or odd
        int a = 5;

        if(a%2==0){
            // ture --> Even
            System.out.println(a + " is even");
        }else{
            // false --> Odd
            System.out.println(a + " is odd");
        }
    }
}
